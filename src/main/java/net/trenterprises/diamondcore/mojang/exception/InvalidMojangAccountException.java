package net.trenterprises.diamondcore.mojang.exception;

/**
 * This exception is thrown whenever a non-existent Mojang
 * account is called by the program or a plugin
 * 
 * @author Trent Summerlin
 * @version 1.0
 */
public class InvalidMojangAccountException extends Exception {
	
	private static final long serialVersionUID = -8587174546303719810L;

	public InvalidMojangAccountException(String AccountName) {
		super("The account " + AccountName + "is not a valid mojang account!");
	}
	
}
